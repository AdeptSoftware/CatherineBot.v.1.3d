class Hero:
    def __init__(self, name, skills, talents, skins):
        self.__name__ = name
        self.skills = skills
        self.talents = talents
        self.skins = skins

    def get_rnd(self, pos):
        index = random.randint(0, 3)
        pos += [index]
        if index == 0:
            return self.skills.get_rnd(pos)
        if index == 1:
            return self.talents.get_rnd(pos)
        if index == 2:
            return self.skins.get_rnd(pos)
        return self.__name__


