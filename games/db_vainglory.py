# информация по игре
import random


class R:
    def __init__(self, data):
        self.x = data

    def get_rnd(self, pos):
        if len(self.x) != 0:
            index = random.randint(0, len(self.x)-1)
            pos += [index]
            res = self.x[index]
            if type(res) is str:
                return res
            return res.get_rnd(pos)
        return ""


